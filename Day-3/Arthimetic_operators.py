# Numbers and Math Operators in Python

# -----------------------------
# 1. Basic Arithmetic Operators
# -----------------------------

# Addition (+) and Subtraction (-)
print(10 + 12)
print(12 - 10)

# Multiplication (*)
print(10 * 3)

# Division (/): Always returns float
print(9 / 2)

# Division (//): Rounds down to the nearest whole number
print(9 // 2)

print(5 * 2.0)

print(10 ** 3)

print((2 ** 3) ** 3)

print(7 % 5)

# -----------------------------
# 3. Operator Precedence
# -----------------------------

print((2 + 4) * 2 ** 3)
# 6 * 2 ** 3 - ()
# 6 * 8 - **
# 48 - * /
# - + -